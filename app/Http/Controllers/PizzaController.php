    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $pizza = Pizza::find($id);

        if (!$pizza) {
            if (!request()->ajax())
                return response()->json([
                    'message' => Lang::get('Pizza not found')
                ], 404);

            abort(404);
        }

        if (request()->ajax()) {
            return response()->json([
                'data' => $pizza
            ]);
        }

        return view('pages.pizzas.show', compact('pizza'));
    }

